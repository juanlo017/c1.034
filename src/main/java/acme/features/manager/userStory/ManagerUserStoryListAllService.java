package acme.features.manager.userStory;

import java.util.Collection;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import acme.client.data.models.Dataset;
import acme.client.services.AbstractService;
import acme.entities.projects.UserStory;
import acme.roles.Manager;

@Service
public class ManagerUserStoryListAllService extends AbstractService<Manager, UserStory> {

	// Internal state ---------------------------------------------------------

	@Autowired
	private ManagerUserStoryRepository repository;

	// AbstractService interface ----------------------------------------------


	@Override
	public void authorise() {
		super.getResponse().setAuthorised(true);
	}

	@Override
	public void load() {
		Collection<UserStory> stories;

		stories = this.repository.findAllUserStories();

		super.getBuffer().addData(stories);
	}

	@Override
	public void unbind(final UserStory stories) {
		assert stories != null;

		Dataset dataset;

		dataset = super.unbind(stories, "title", "description", "estimatedCost");

		super.getResponse().addData(dataset);
	}

}
